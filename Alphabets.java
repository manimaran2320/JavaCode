package Assignments;

public class Alphabets {
	public static void main(String[] args) {
		char i;
		
		//Lower case alphabets
		for(i='a';i<='z';i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		
		//Upper case alphabets
		for(i='A';i<='Z';i++) {
			System.out.print(i+" ");
		}
	}

}
